export function computeRoomSummary(visionDataJson: string): string {
  try {
    const data = JSON.parse(visionDataJson) as {
      rooms?: Array<{
        name: string;
        area?: number | null;
        length?: number | null;
        width?: number | null;
      }>;
      totalArea?: number | null;
    };

    const rooms = data.rooms || [];
    if (rooms.length === 0) return "";

    const lines: string[] = [];
    let calcTotal = 0;
    let allExact = true;

    for (const room of rooms) {
      const area =
        room.area != null
          ? room.area
          : room.length && room.width
          ? Math.round(room.length * room.width * 10) / 10
          : null;

      const hasExact = !!(room.length && room.width);
      if (!hasExact) allExact = false;

      let perimeter: number | null = null;
      if (room.length && room.width) {
        perimeter = 2 * (room.length + room.width);
      } else if (area) {
        // estimate via 1.3:1 room proportion
        const l = Math.sqrt(area * 1.3);
        const w = Math.sqrt(area / 1.3);
        perimeter = 2 * (l + w);
      }

      if (area) calcTotal += area;

      const areaStr = area != null ? `${area} м²` : "площадь неизвестна";
      const perimStr =
        perimeter != null
          ? `, периметр ${hasExact ? "" : "~"}${perimeter.toFixed(1)} м.п.`
          : "";

      lines.push(`• ${room.name} — ${areaStr}${perimStr}`);
    }

    const totalArea = data.totalArea ?? calcTotal;
    const n = rooms.length;
    const roomWord = n === 1 ? "комнату" : n < 5 ? "комнаты" : "комнат";

    const parts = [
      `Нашёл ${n} ${roomWord}:`,
      ...lines,
      `\nОбщая площадь: ${totalArea.toFixed(1)} м²`,
    ];

    if (!allExact) {
      parts.push(
        "⚠️ Периметры приблизительные (точные размеры стен в паспорте не указаны)"
      );
    } else {
      parts.push("Размеры точные ✓");
    }

    return parts.join("\n");
  } catch {
    return "";
  }
}

